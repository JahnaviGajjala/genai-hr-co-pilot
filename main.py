from fastapi import FastAPI
from pydantic import BaseModel
import pandas as pd
import joblib
import os
from app.qa_co_pilot import answer_question
from retrain import retrain_model  # ðŸ”¹ import retraining function

# â”€â”€â”€ Paths â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
MODEL_DIR = os.path.join(BASE_DIR, "app", "model")
model_path = os.path.join(MODEL_DIR, "isolation_forest_model.pkl")
scaler_path = os.path.join(MODEL_DIR, "payroll_scaler.pkl")

# â”€â”€â”€ Load Model and Scaler â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
model = joblib.load(model_path)
scaler = joblib.load(scaler_path)

# â”€â”€â”€ Create FastAPI App â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app = FastAPI(title="Payroll Anomaly Detector")

# â”€â”€â”€ Input Schema â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
class PayrollInput(BaseModel):
    BaseSalary: float
    Bonus: float
    Deductions: float

class Question(BaseModel):
    query: str

# â”€â”€â”€ Predict Endpoint â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.post("/predict")
def predict(input_data: PayrollInput):
    input_dict = input_data.dict()
    input_df = pd.DataFrame([input_dict])
    scaled_input = scaler.transform(input_df)
    prediction = model.predict(scaled_input)
    return {"anomaly": bool(prediction[0])}

# â”€â”€â”€ LLM HR Co-Pilot â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.post("/ask")
def ask_question(q: Question):
    try:
        response = answer_question(q.query)
        return {"answer": response}
    except Exception as e:
        return {"error": str(e)}

# â”€â”€â”€ Retrain Endpoint â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.post("/retrain")
def retrain():
    try:
        result = retrain_model()
        # ðŸ”„ Reload updated model and scaler into memory
        global model, scaler
        model = joblib.load(model_path)
        scaler = joblib.load(scaler_path)
        return result
    except Exception as e:
        return {"status": "error", "message": str(e)}
